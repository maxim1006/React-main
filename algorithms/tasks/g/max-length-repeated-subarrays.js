function findLength(nums1, nums2) {
    let l1 = nums1.length;
    let l2 = nums2.length;
    let result = 0;

    for (let i = 0; i < l1; i++) {
        for (let j = 0; j < l2; j++) {
            result = Math.max(result, helper(nums1, nums2, l1, l2, i, j));
        }
    }

    return result;
}

function helper(nums1, nums2, l1, l2, i, j) {
    if (i >= l1 || j >= l2 || nums1[i] !== nums2[j]) return 0;

    let result = 1;

    result += helper(nums1, nums2, l1, l2, i + result, j + result);

    return result;
}

